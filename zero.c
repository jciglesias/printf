#include "ft_printf.h"

int	ft_void0(t_flags x)
{
	int a;

	a = 0;
	if (!x.minus)
		while (x.width-- > 5)
			a += write(1, " ", 1);
	a += write(1, "(nil)", 5);
	if (x.minus)
		while (x.width-- > 5)
			a += write(1, " ", 1);
	return (a);
}
